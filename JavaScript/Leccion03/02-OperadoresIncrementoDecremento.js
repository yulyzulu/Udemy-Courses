// Operators
let a = 3, b = 2;

let z = a + b;

// Incremento
// Pre-incremento (el operador ++ antes de la variable)
z = ++a;
console.log(a); // 4
console.log(z); // 4

// Post-incremento (el operador ++ despu√©s de la variable)
z = b++;
console.log(b); // 3
console.log(z); // 2

// Decremento
// Pre-decremento
z = --a;
console.log(a); // 3
console.log(z); // 3

//Post decremento
z = b--;
console.log(b); // 2
console.log(z); // 3